char	*ft_strrchr(const char *s, int c)
{
	char *chr;

	chr = 0;
	while (*s)
	{
		if (*s == (char)c)
			chr = (char *)s;
		s++;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (chr);
}
