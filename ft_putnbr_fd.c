#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > '9')
	{
		n = n / 10;
		i = n % 10;
		ft_putnbr_fd(n, fd);
		ft_putnbr_fd(i, fd);
	}
	if (n >= '0' && n <= '9')
		write(fd, &n, 1);
}
